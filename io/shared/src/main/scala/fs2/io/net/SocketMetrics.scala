package fs2
package io
package net

sealed trait SocketMetrics {

  /** Total number of bytes read from the network through this socket. */
  def totalBytesRead(): Long

  /** Total number of bytes written to the network through this socket. */
  def totalBytesWritten(): Long

  /** Number of times a write request consumed only part of the write buffer. */
  def incompleteWriteCount(): Long
}

object SocketMetrics {
  private[net] trait UnsealedSocketMetrics extends SocketMetrics
}
